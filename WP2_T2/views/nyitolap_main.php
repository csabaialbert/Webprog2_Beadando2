<h1>
    Üdvözöljük a honlapunkon!
</h1>
<p>
    A honlapunk elsődleges feladata a hazánkban található összes tanösvény áttekinthető  adatainak elérhetővé tétele az Önök számára. 
    Ezek az adatok egy adatbázisban találhatóak. A megtekintésükhöz be kell jelentkeznie, amit a belépés menüpont alatt megtehet.
    Amennyiben nem rendelkezik felhasználóval, regisztrálhat oldalunkra szintén a belépés menüpont alatt, a regisztrációs adatok kitöltésével.
    Belépés után minden felhasználói tartalom elérhetővé válik Ön számára. 
    Emellett található egy külsős REST API teszt kliens is, mely a "gorest.co.in" API-t használja.
    Egy másik oldalunkon taláható egy PDF készítő szolgáltatás, mely a megadott alapján az ahhoz tartozó adatokat egy PDF kiterjesztésű adatállományban letölthetővé teszi azokat az Ön számára.
    Egy az adatbázis adataiból készült grafikon is elérhető a menüpontok alatt.
    Jó munkát kívánunk oldalunkon!
</p>
